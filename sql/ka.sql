-- 1. DATABASE STRUCTUUR (DDL)
-- =======================================================

SET FOREIGN_KEY_CHECKS = 0;
DROP TABLE IF EXISTS ka_weken, ka_periodes, ka_week_types, ka_schooljaren;
SET FOREIGN_KEY_CHECKS = 1;

CREATE TABLE ka_schooljaren (
    id INT PRIMARY KEY AUTO_INCREMENT,
    naam VARCHAR(50) NOT NULL UNIQUE,
    startdatum DATE NOT NULL,
    einddatum DATE NOT NULL,
    CONSTRAINT chk_sj_datums CHECK (einddatum > startdatum)
) ENGINE=InnoDB;

CREATE TABLE ka_periodes (
    id INT PRIMARY KEY AUTO_INCREMENT,
    schooljaar_id INT NOT NULL,
    volgnummer INT NOT NULL,
    naam VARCHAR(50) NOT NULL,
    FOREIGN KEY (schooljaar_id) REFERENCES ka_schooljaren(id) ON DELETE CASCADE,
    UNIQUE (schooljaar_id, volgnummer)
) ENGINE=InnoDB;

CREATE TABLE ka_week_types (
    id INT PRIMARY KEY AUTO_INCREMENT,
    is_lesweek BOOLEAN NOT NULL DEFAULT FALSE,
    omschrijving VARCHAR(100) NOT NULL UNIQUE
) ENGINE=InnoDB;

CREATE TABLE ka_weken (
    id INT PRIMARY KEY AUTO_INCREMENT,
    schooljaar_id INT NOT NULL,
    periode_id INT DEFAULT NULL,
    volgnummer_schooljaar INT NOT NULL,
    kalenderweek INT NOT NULL,
    startdatum DATE NOT NULL,
    einddatum DATE NOT NULL,
    type_id INT NOT NULL,
    FOREIGN KEY (schooljaar_id) REFERENCES ka_schooljaren(id) ON DELETE CASCADE,
    FOREIGN KEY (periode_id) REFERENCES ka_periodes(id) ON DELETE SET NULL,
    FOREIGN KEY (type_id) REFERENCES ka_week_types(id),
    CONSTRAINT chk_week_datums CHECK (einddatum >= startdatum),
    INDEX idx_datum_range (startdatum, einddatum)
) ENGINE=InnoDB;

