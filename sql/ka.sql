-- =======================================================
-- 1. TABELLEN AANMAKEN
-- =======================================================

CREATE TABLE ka_schooljaren (
    id INT PRIMARY KEY AUTO_INCREMENT,
    naam VARCHAR(50) NOT NULL,
    startdatum DATE NOT NULL,
    einddatum DATE NOT NULL
);

CREATE TABLE ka_periodes (
    id INT PRIMARY KEY AUTO_INCREMENT,
    schooljaar_id INT NOT NULL,
    volgnummer INT NOT NULL,
    naam VARCHAR(50) NOT NULL,
    FOREIGN KEY (schooljaar_id) REFERENCES ka_schooljaren(id)
);

CREATE TABLE ka_week_types (
    id INT PRIMARY KEY AUTO_INCREMENT,
    is_lesweek BOOLEAN NOT NULL DEFAULT FALSE,
    omschrijving VARCHAR(100) NOT NULL
);

CREATE TABLE ka_weken (
    id INT PRIMARY KEY AUTO_INCREMENT,
    schooljaar_id INT NOT NULL,
    periode_id INT,
    volgnummer_schooljaar INT NOT NULL,
    kalenderweek INT NOT NULL,
    startdatum DATE NOT NULL,
    einddatum DATE NOT NULL,
    type_id INT NOT NULL,
    FOREIGN KEY (schooljaar_id) REFERENCES ka_schooljaren(id),
    FOREIGN KEY (periode_id) REFERENCES ka_periodes(id),
    FOREIGN KEY (type_id) REFERENCES ka_week_types(id)
);

